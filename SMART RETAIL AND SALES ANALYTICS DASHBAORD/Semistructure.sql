CREATE OR REPLACE TASK SMART_RETAIL_INDIA.RAW_LAYER.TASK_FACT_SALES
    WAREHOUSE = COMPUTE_WH
    SCHEDULE  = '1 MINUTE'
WHEN
    SYSTEM$STREAM_HAS_DATA('SMART_RETAIL_INDIA.RAW_LAYER.STREAM_FACT_SALES')
AS
INSERT INTO SMART_RETAIL_INDIA.ANALYTICS.FACT_SALES
(
    SALES_ID, DATE_ID, PRODUCT_ID, CUSTOMER_ID, SALESPERSON_ID,
    PROMOTION_ID, PAYMENT_ID,QUANTITY, UNIT_PRICE, SALES_AMOUNT, COST, PROFIT
)
SELECT
    TRIM(SALES_ID),
    TRY_CAST(DATE_ID        AS INT),
    TRIM(PRODUCT_ID),
    TRIM(CUSTOMER_ID),
    TRIM(SALESPERSON_ID),
    TRIM(PROMOTION_ID),
    TRIM(PAYMENT_ID),
    TRY_CAST(QUANTITY       AS INT),
    TRY_CAST(UNIT_PRICE     AS NUMBER(10,2)),
    TRY_CAST(SALES_AMOUNT   AS NUMBER(15,2)),
    TRY_CAST(COST           AS NUMBER(15,2)),
    TRY_CAST(PROFIT         AS NUMBER(15,2))
FROM SMART_RETAIL_INDIA.RAW_LAYER.STREAM_FACT_SALES
WHERE METADATA$ACTION = 'INSERT'
  AND TRY_CAST(DATE_ID AS INT)                  IS NOT NULL
  AND TRY_CAST(QUANTITY AS INT)                 IS NOT NULL
  AND TRY_CAST(SALES_AMOUNT AS NUMBER(15,2))    IS NOT NULL
  AND TRIM(SALES_ID) NOT IN (
        SELECT SALES_ID FROM SMART_RETAIL_INDIA.ANALYTICS.FACT_SALES
  );

  ALTER TASK SMART_RETAIL_INDIA.RAW_LAYER.TASK_FACT_SALES RESUME;

  SHOW TASKS IN SCHEMA SMART_RETAIL_INDIA.RAW_LAYER;

  SELECT COUNT(*) FROM SMART_RETAIL_INDIA.ANALYTICS.FACT_SALES;
  SELECT COUNT(*) FROM SMART_RETAIL_INDIA.RAW_LAYER.FACT_SALES;
  --select * from STREAM_FACT_SALES;

  SELECT *
FROM TABLE(SMART_RETAIL_INDIA.INFORMATION_SCHEMA.COPY_HISTORY(
    TABLE_NAME => 'FACT_SALES',
    START_TIME => DATEADD(HOUR, -1, CURRENT_TIMESTAMP)
))
ORDER BY LAST_LOAD_TIME DESC;

SELECT PARSE_JSON(SYSTEM$PIPE_STATUS('SMART_RETAIL_INDIA.RAW_LAYER.PIPE_FACT_SALES'));

