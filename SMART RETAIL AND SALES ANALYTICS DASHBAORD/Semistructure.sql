WAREHOUSE = COMPUTE_WH
SCHEDULE = '1 MINUTE'
WHEN SYSTEM$STREAM_HAS_DATA('SMART_RETAIL_INDIA.RAW_LAYER.STREAM_DIM_DATE')
AS
INSERT INTO SMART_RETAIL_INDIA.ANALYTICS.DIM_DATE
(DATE_ID, FULL_DATE, DAY_NAME, DAY_OF_MONTH,
 MONTH, QUARTER, YEAR, FISCAL_YEAR,
 IS_WEEKEND, IS_HOLIDAY, HOLIDAY_NAME)
SELECT
    S.RAW_DATA:DATE_ID::INT,
    S.RAW_DATA:FULL_DATE::DATE,
    S.RAW_DATA:DAY_NAME::VARCHAR,
    S.RAW_DATA:DAY_OF_MONTH::INT,
    S.RAW_DATA:MONTH::VARCHAR,
    S.RAW_DATA:QUARTER::VARCHAR,
    S.RAW_DATA:YEAR::INT,
    S.RAW_DATA:FISCAL_YEAR::VARCHAR,
    S.RAW_DATA:IS_WEEKEND::BOOLEAN,
    S.RAW_DATA:IS_HOLIDAY::BOOLEAN,
    S.RAW_DATA:HOLIDAY_NAME::VARCHAR
FROM SMART_RETAIL_INDIA.RAW_LAYER.STREAM_DIM_DATE S
WHERE S.METADATA$ACTION = 'INSERT'
AND S.RAW_DATA:DATE_ID::INT IS NOT NULL
AND NOT EXISTS (
    SELECT 1 FROM SMART_RETAIL_INDIA.ANALYTICS.DIM_DATE A
    WHERE A.DATE_ID = S.RAW_DATA:DATE_ID::INT
);
ALTER TASK SMART_RETAIL_INDIA.RAW_LAYER.TASK_DIM_DATE RESUME;



